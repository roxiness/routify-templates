<h1>Something about HTML</h1>
<li>
    Most elements take the language-related attribute dir to specify text
    direction, such as with "rtl" for right-to-left text in, for example,
    Arabic, Persian or Hebrew.
</li>

<li>
    As of version 4.0, HTML defines a set of 252 character entity references and
    a set of 1,114,050 numeric character references, both of which allow
    individual characters to be written via simple markup, rather than
    literally. A literal character and its markup counterpart are considered
    equivalent and are rendered identically.
</li>

<li>
    HTML defines several data types for element content, such as script data and
    stylesheet data, and a plethora of types for attribute values, including
    IDs, names, URIs, numbers, units of length, languages, media descriptors,
    colors, character encodings, dates and times, and so on. All of these data
    types are specializations of character data.
</li>
